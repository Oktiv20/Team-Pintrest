import { clientCredentials } from '../utils/client';

const dbUrl = clientCredentials.databaseURL;

const getPins = (uid) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/pins/.json?orderBy="uid"&equalTo="${uid}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

const createPin = (payload) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/pins.json`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});
const getSinglePin = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/pins/${firebaseKey}.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const deleteSinglePin = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/pins/${firebaseKey}.json`, {
    method: 'DELETE',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const updatePin = (payload) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/pins/${payload.firebaseKey}.json`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then(resolve)
    .catch(reject);
});

const getPinPinned = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/pins.json?orderBy="author_id"&equalTo="${firebaseKey}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(Object.values(data)))
    .catch(reject);
});

export {
  getPins,
  createPin,
  getSinglePin,
  deleteSinglePin,
  updatePin,
  getPinPinned,
};
