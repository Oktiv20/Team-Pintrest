import { clientCredentials } from '../utils/client';

const dbUrl = clientCredentials.databaseURL;

const createUserPin = (payload) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/pins.json`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => {
      const setcode = { firebaseKey: data.name };
      fetch(`${dbUrl}/pins/${setcode.firebaseKey}.json`, {
        method: 'PATCH',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify(setcode),
      }).then(resolve);
    })
    .catch(reject);
});

const getUserPins = (uid) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/pins.json?orderBy="uid"&equalTo="${uid}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

const getSingleUserPin = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/pins/${firebaseKey}.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data)) // will resolve a single object
    .catch(reject);
});

const updateUserPin = (payload) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/pins/${payload.firebaseKey}.json`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then(resolve)
    .catch(reject);
});

const deleteUserPin = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/saved/${firebaseKey}.json`, {
    method: 'DELETE',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const createSavedPin = (payload) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/saved.json`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const getPinPinned = (uid) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/saved.json?orderBy="uid"&equalTo="${uid}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(Object.values(data)))
    .catch(reject);
});

export {
  createUserPin,
  getUserPins,
  getSingleUserPin,
  updateUserPin,
  deleteUserPin,
  createSavedPin,
  getPinPinned,
};
